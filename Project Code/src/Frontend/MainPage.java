    public void linechart(){
        lineChart2.addLegend("Question Target", new Color(12, 84, 175), new Color(0, 108, 247));
        
        Date nowtime = new Date();
        DateFormat df = new SimpleDateFormat("yyyy/MM/dd");
        String nowtime2=df.format(nowtime);
        String time_split[]=nowtime2.split("/");
        int month=Integer.parseInt(time_split[1]);
        int month3=month;
        month3-=6;

        String months[]={"January","February","March","April","May","June","July","August","September","October","November","December"};

        int count=0;
        
        while(count<6){
     
            int target_count=0;
          if(month3<0){
                
                month3=0;
               
            }
            
            String query="Select * from Goals where user_id = "+user.getId();
            try {
                db.sqlquery=db.con.createStatement();
                ResultSet res=db.sqlquery.executeQuery(query);
                while(res.next()){
                    
                    String date=res.getString("gdate");
                    String date_split[]=date.split("/");
                    int month2=Integer.parseInt(date_split[1]);
                    
                    if(month2==month3+1){
                       target_count+=res.getInt("qcount");

                    }
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
            }
               lineChart2.addData(new ModelChart(months[month3], new double[]{target_count}));
                  count++;
                  month3++;
            
            
        }
        
        lineChart2.start();
        
    }
    
    
