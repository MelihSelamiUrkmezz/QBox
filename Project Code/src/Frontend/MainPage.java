    public void lastsixmonths(){
       
        Date nowtime = new Date();
        DateFormat df = new SimpleDateFormat("yyyy/MM/dd");
        String nowtime2=df.format(nowtime);
        String time_split[]=nowtime2.split("/");
        int month=Integer.parseInt(time_split[1]);
        int month3=month;
        month3-=6;
        
        chart1.addLegend("QCount", new Color(12, 84, 175), new Color(0, 108, 247));
        chart1.addLegend("True", new Color(54, 4, 143), new Color(104, 49, 200));
        chart1.addLegend("False", new Color(5, 125, 0), new Color(95, 209, 69));
        chart1.addLegend("Empty", new Color(186, 37, 37), new Color(241, 100, 120));

        String months[]={"January","February","March","April","May","June","July","August","September","October","November","December"};

        int count=0;
        
        while(count<6){
            int totalcount=0;
            int truecount=0;
            int falsecount=0;
            int emptycount=0;
          if(month3<0){
                
                month3=0;
               
            }
            
            String query="Select * from Questions where user_id = "+user.getId();
            try {
                db.sqlquery=db.con.createStatement();
                ResultSet res=db.sqlquery.executeQuery(query);
                while(res.next()){
                    
                    String date=res.getString("udate");
                    String date_split[]=date.split("/");
                    int month2=Integer.parseInt(date_split[1]);
                    
                    if(month2==month3+1){
                        totalcount+=res.getInt("qcount");
                        truecount+=res.getInt("truec");
                        falsecount+=res.getInt("falsec");
                        emptycount+=res.getInt("emptyc");

                    }
                }
                  chart1.addData(new ModelChart(months[month3], new double[]{totalcount,truecount,falsecount,emptycount}));
                  count++;
                  month3++;
                
            } catch (SQLException ex) {
                Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
            
            
            
        }
        
        chart1.start();
        
    }
    
    
