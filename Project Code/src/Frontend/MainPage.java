    public void showGoalPieChart(){
        
        DefaultPieDataset pieDataset=new DefaultPieDataset();
        ArrayList<Goal> goals=user.getTotalLessonsGoalNowCount();
        
        int totalcount=0;
        
        for(int i=0;i<goals.size();i++){
            
            totalcount+=goals.get(i).getqCount();
            
        }
        for(int i=0;i<goals.size();i++){
            
            String query="Select * from Lessons where id = ?";
            
            try {
                db.psqlquery=db.con.prepareStatement(query);
                db.psqlquery.setInt(1,goals.get(i).getLesson_id());
                String lessons="";
                ResultSet res=db.psqlquery.executeQuery();
                
                while(res.next()){
                    
                    lessons=res.getString("Lessons");
                    
                }
                pieDataset.setValue(lessons,((float)goals.get(i).getqCount()/(float)totalcount)*100);
            } catch (SQLException ex) {
                Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
        JFreeChart chart=ChartFactory.createPieChart("", pieDataset,true,true,true);
        PiePlot P=(PiePlot)chart.getPlot();
        P.setBackgroundPaint(new Color(60,63,65));
        ChartPanel panel;
        panel=new ChartPanel(chart);
        panel.setOpaque(false);
        chart.getPlot().setBackgroundPaint(new Color(60,63,65));
        piece.removeAll();
        piece.add(panel, BorderLayout.CENTER);
        piece.validate();
    }
    
